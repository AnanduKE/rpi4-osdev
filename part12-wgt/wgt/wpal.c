#include "../include/wgt.h"

void wloadpalette (unsigned char *data, color *pal)
{
  unsigned char r, g, b;
  int i;

  if  (data == NULL)
  { 
    return;
  }

  for  (i = 0; i < 256; i++)           /* Load in RGB values */
  {
    r = 4 * *data++;
    g = 4 * *data++;
    b = 4 * *data++;
    wsetrgb (i, r, g, b, pal);         /* Set current palette */
  }
}

